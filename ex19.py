def cheese_and_crackers(cheese_count, boxes_of_crackers):
	print(f"You have {cheese_count} cheeses!")
	print(f"You have {boxes_of_crackers} boxes of crackers!")
	print("Man that's enough for a party!")
	print("Get a blanket")

print("We can just give the functions numbers directly:")
#variable for cheese and crackers where values are placed back in the function
cheese_and_crackers(20, 30)

print("Or, we can use the variables from our script:")
#variable for amount of cheese
amount_of_cheese = 10
#variable for amount of crackers
amount_of_crackers = 50

#variable for cheese and crackers which contains amount of cheese and amount of crackers
cheese_and_crackers(amount_of_cheese, amount_of_crackers)

#variable for cheese and crackers where math is being done
print("We can even do math inside too:")
cheese_and_crackers(10 + 20, 5 + 6)


print("And we can combine the two, variables and math:")
cheese_and_crackers(amount_of_cheese + 100, amount_of_crackers + 100)

